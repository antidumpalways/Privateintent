/**
 * Ika Multi-Chain dWallet Service
 *
 * Supports native signing for:
 *   - Ethereum (Secp256k1 + EcdsaKeccak256)
 *   - Bitcoin  (Secp256k1 + EcdsaDoubleSha256 / TaprootSha256)
 *   - Solana   (Curve25519 + EddsaSha512)
 *   - Polkadot (Ristretto + SchnorrkelMerlin)
 *
 * NO FALLBACK TO SIMULATION. All operations hit the real Ika pre-alpha devnet.
 * On failure: retries with exponential backoff, then throws a hard error.
 */

import * as grpc from "@grpc/grpc-js";
import { BinaryWriter, BinaryReader } from "@bufbuild/protobuf/wire";
import { randomBytes } from "crypto";

const IKA_GRPC_URL =
  process.env.IKA_GRPC_URL ?? "pre-alpha-dev-1.ika.ika-network.net:443";

// ─────────────────────────────────────────────────────────────────────────────
// Protobuf encode/decode — using @bufbuild/protobuf/wire (identical to
// DWalletServiceClient generated by protoc-gen-ts_proto).
// ─────────────────────────────────────────────────────────────────────────────

function encodeUserSignedRequest(userSig: Uint8Array, signedData: Uint8Array): Buffer {
  const w = new BinaryWriter();
  if (userSig.length > 0) w.uint32(10).bytes(userSig);
  if (signedData.length > 0) w.uint32(18).bytes(signedData);
  return Buffer.from(w.finish());
}

function decodeTransactionResponse(b: Buffer): Uint8Array {
  const reader = new BinaryReader(b);
  while (reader.pos < reader.len) {
    const tag = reader.uint32();
    if ((tag >>> 3) === 1) return reader.bytes();
    reader.skip(tag & 7);
  }
  return new Uint8Array(0);
}

// ─────────────────────────────────────────────────────────────────────────────
// gRPC client
// ─────────────────────────────────────────────────────────────────────────────

let _rawClient: InstanceType<ReturnType<typeof grpc.makeGenericClientConstructor>> | null = null;

function getRawGrpcClient() {
  if (_rawClient) return _rawClient;
  const creds =
    IKA_GRPC_URL.includes("localhost") || IKA_GRPC_URL.includes("127.0.0.1")
      ? grpc.credentials.createInsecure()
      : grpc.credentials.createSsl();

  const ServiceCtor = grpc.makeGenericClientConstructor(
    {
      SubmitTransaction: {
        path: "/ika.dwallet.v1.DWalletService/SubmitTransaction",
        requestStream: false,
        responseStream: false,
        requestSerialize: (req: { userSignature: Buffer; signedRequestData: Buffer }) =>
          encodeUserSignedRequest(req.userSignature, req.signedRequestData),
        requestDeserialize: (b: Buffer) => b,
        responseSerialize: (b: Buffer) => b,
        responseDeserialize: (b: Buffer) => decodeTransactionResponse(b),
      },
    },
    "DWalletService",
    {},
  );
  _rawClient = new ServiceCtor(IKA_GRPC_URL, creds) as any;
  return _rawClient!;
}

function submitTransaction(userSig: Uint8Array, signedData: Uint8Array): Promise<Uint8Array> {
  return new Promise((resolve, reject) => {
    const client = getRawGrpcClient();
    (client as any).SubmitTransaction(
      { userSignature: Buffer.from(userSig), signedRequestData: Buffer.from(signedData) },
      (err: (Error & { code?: number; details?: string }) | null, resp: Uint8Array) => {
        if (err) {
          const detail = [err.message, err.code !== undefined ? `grpc_code=${err.code}` : "", err.details ? `details=${err.details}` : ""]
            .filter(Boolean).join(" | ");
          reject(new Error(detail));
        } else {
          resolve(new Uint8Array(resp));
        }
      },
    );
  });
}

/** Single-attempt timeout wrapper. */
function withTimeout<T>(p: Promise<T>, ms: number): Promise<T> {
  return Promise.race([
    p,
    new Promise<never>((_, reject) =>
      setTimeout(() => reject(new Error(`Ika gRPC timeout after ${ms}ms`)), ms),
    ),
  ]);
}

/**
 * Retry with exponential backoff.
 * maxAttempts = 0 means retry forever.
 * Throws on exhaustion only when maxAttempts > 0.
 */
async function retryWithBackoff<T>(
  label: string,
  fn: (attempt: number) => Promise<T>,
  opts: { maxAttempts: number; baseDelayMs: number; maxDelayMs: number },
): Promise<T> {
  let attempt = 0;
  while (true) {
    attempt++;
    try {
      return await fn(attempt);
    } catch (err) {
      const msg = err instanceof Error ? err.message : String(err);
      const isLast = opts.maxAttempts > 0 && attempt >= opts.maxAttempts;

      process.stderr.write(
        `[IkaMultichain] ${label} attempt ${attempt}${opts.maxAttempts > 0 ? `/${opts.maxAttempts}` : ""} FAILED: ${msg}\n`,
      );

      if (isLast) {
        throw new Error(`[Ika] ${label} failed after ${attempt} attempts. Last error: ${msg}`);
      }

      const delay = Math.min(opts.baseDelayMs * Math.pow(1.5, attempt - 1), opts.maxDelayMs);
      process.stderr.write(`[IkaMultichain] ${label} retrying in ${Math.round(delay)}ms…\n`);
      await new Promise(r => setTimeout(r, delay));
    }
  }
}

